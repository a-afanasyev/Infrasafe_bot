-- Должно вернуть 0


-- Проверяем распределение заявок по статусам
SELECT
    status,
    COUNT(*) as count,
    ROUND(COUNT(*) * 100.0 / SUM(COUNT(*)) OVER (), 2) as percentage
FROM requests
GROUP BY status
ORDER BY count DESC;


