-- Посмотреть сколько заявок со статусом "Принята"
SELECT COUNT(*) as count_accepted
FROM requests
WHERE status = 'Принята';

-- Посмотреть детали этих заявок
SELECT
    request_number,
    status,
    category,
    created_at,
    executor_id,
    user_id
FROM requests
WHERE status = 'Принята'
ORDER BY created_at DESC
LIMIT 20;

-- ===================================================================
-- ШАГ 2: Миграция данных
-- ===================================================================

-- ВАРИАНТ 1: Перевести все заявки "Принята" в статус "В работе"
-- Это логичный выбор, так как статус "Принята" означал, что менеджер принял заявку
-- и она готова к назначению исполнителю

BEGIN;

-- Обновляем заявки со статусом "Принята" на "В работе"
UPDATE requests
SET status = 'В работе',
    updated_at = NOW()
WHERE status = 'Принята';

-- Проверяем результат
SELECT COUNT(*) as migrated_count
FROM requests
WHERE status = 'В работе'
AND updated_at >= NOW() - INTERVAL '1 minute';

