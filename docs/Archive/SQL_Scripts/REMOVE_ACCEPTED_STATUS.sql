-- ШАГ 3: Обновление constraint для таблицы requests
-- ===================================================================

-- Удаляем старый constraint
BEGIN;

ALTER TABLE requests
DROP CONSTRAINT IF EXISTS check_request_status;

-- Создаем новый constraint без статуса "Принята"
-- Добавлены новые статусы "Исполнено" и "Принято"
ALTER TABLE requests
ADD CONSTRAINT check_request_status
CHECK (status IN (
    'Новая',
    'В работе',
    'Закуп',
    'Уточнение',
    'Выполнена',
    'Исполнено',
    'Принято',
    'Отменена'
));

COMMIT;


